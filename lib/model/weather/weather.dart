class Weather {
  final int id;
  final String main;
  final String description;
  final String icon;

  Weather({
    required this.id,
    required this.main,
    required this.description,
    required this.icon,
  });

  factory Weather.fromJsonMap(List<dynamic> json) => Weather(
        id: json[0]["id"],
        main: json[0]["main"],
        description: json[0]["description"],
        icon: json[0]["icon"],
      );

  Map<String, dynamic> toJson() => {
        "id": id,
        "main": main,
        "description": description,
        "icon": icon,
      };
}
